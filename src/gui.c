void gui_init() {
    system_gui.window_count = 0;
    system_gui.desktop_color = 1; // Blue
    vga_set_mode_13h();
}

void gui_draw_desktop() {
    vga_clear(system_gui.desktop_color);
    // Draw taskbar
    vga_fill_rect(0, VGA_HEIGHT - 24, VGA_WIDTH, 24, 7);
    gui_draw_string(10, VGA_HEIGHT - 18, "SkyNet OS", 0);
}

void gui_create_window(const char* title, int x, int y, int w, int h, color_t color) {
    if (system_gui.window_count < MAX_WINDOWS) {
        window_t* win = &system_gui.windows[system_gui.window_count++];
        win->x = x; win->y = y; win->w = w; win->h = h;
        win->bg_color = color;
        strncpy(win->title, title, 31);
        win->active = true;
    }
}

void gui_draw_window(window_t* win) {
    if (!win->active) return;
    // Shadow
    vga_fill_rect(win->x + 4, win->y + 4, win->w, win->h, 0);
    // Window body
    vga_fill_rect(win->x, win->y, win->w, win->h, 7);
    // Title bar
    vga_fill_rect(win->x + 1, win->y + 1, win->w - 2, 14, 1);
    gui_draw_string(win->x + 8, win->y + 4, win->title, 15);
    // Close button [X]
    vga_fill_rect(win->x + win->w - 14, win->y + 2, 12, 10, 4); // Red
    gui_draw_char(win->x + win->w - 12, win->y + 4, 'X', 15);
    // Content area
    vga_fill_rect(win->x + 1, win->y + 15, win->w - 2, win->h - 16, win->bg_color);
}

void gui_render() {
    gui_draw_desktop();
    for (int i = 0; i < system_gui.window_count; i++) {
        gui_draw_window(&system_gui.windows[i]);
    }
}

void gui_draw_char(int x, int y, char c, color_t color) {
    if (x + 8 >= VGA_WIDTH || y + 8 >= VGA_HEIGHT) return;
    if (c < 32 || c > 126) return;

    int index = c - 32;
    const uint8_t *glyph = font8x8[index];

    for (int i = 0; i < 8; i++) {
        uint8_t row = glyph[i];
        for (int j = 0; j < 8; j++) {
            if (row & (0x80 >> j)) {
                vga_put_pixel(x + j, y + i, color);
            }
        }
    }
}

void gui_draw_string(int x, int y, const char* str, color_t color) {
    while (*str) {
        gui_draw_char(x, y, *str++, color);
        x += 8;
    }
}
